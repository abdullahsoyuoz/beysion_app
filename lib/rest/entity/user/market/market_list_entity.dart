import 'package:beysion/rest/persistent.dart';


class MarketListEntity extends Persistent{
  MarketListEntity({
    this.id,
    this.name,
  });

  int id;
  String name;

  factory MarketListEntity.fromJson(Map<String, dynamic> json) => MarketListEntity(
    id: json["id"] == null ? null : json["id"],
    name: json["name"] == null ? null : json["name"],
  );

  Map<String, dynamic> toJson() => {
    "id": id == null ? null : id,
    "name": name == null ? null : name,
  };
}


